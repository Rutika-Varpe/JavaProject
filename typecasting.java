public class typecasting 
{
    public static void main(String[] args) 
    {
        // type casting
        // 1 = automatic 
        byte b = 10;
        int a = b;
        System.out.println(a);

        // type casting
        // 2 = explicit
        
        float f = 20.5f;
        int c = (int)f;

        System.out.println(c);
    }
}
